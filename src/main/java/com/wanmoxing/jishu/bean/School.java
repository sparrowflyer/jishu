package com.wanmoxing.jishu.bean;

public class School {
	
	private int id;
	private String enName;
	private String cnName;
	private String country;
	private String location;
	private String officialSite;
	private String description;
	private int rank;
	private String iconImage;
	private String homeImage;
	private String dataBasic;
	private String dataApply;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEnName() {
		return enName;
	}
	public void setEnName(String enName) {
		this.enName = enName;
	}
	public String getCnName() {
		return cnName;
	}
	public void setCnName(String cnName) {
		this.cnName = cnName;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getOfficialSite() {
		return officialSite;
	}
	public void setOfficialSite(String officialSite) {
		this.officialSite = officialSite;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getIconImage() {
		return iconImage;
	}
	public void setIconImage(String iconImage) {
		this.iconImage = iconImage;
	}
	public String getHomeImage() {
		return homeImage;
	}
	public void setHomeImage(String homeImage) {
		this.homeImage = homeImage;
	}
	public String getDataBasic() {
		return dataBasic;
	}
	public void setDataBasic(String dataBasic) {
		this.dataBasic = dataBasic;
	}
	public String getDataApply() {
		return dataApply;
	}
	public void setDataApply(String dataApply) {
		this.dataApply = dataApply;
	}
	
}
